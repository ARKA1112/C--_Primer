#include <iostream>
using namespace std;

void sizer()
{
    cout<< "size of char is "<< sizeof(char)<<'\n';
    cout<< "size of int is " << sizeof(int) <<'\n';
    cout<< "size of float is "<< sizeof(float) <<'\n';
    cout<< "size of bool is " << sizeof(bool) <<'\n';
    cout<< "size of long is " << sizeof(long) <<'\n';
}

int main()
{
    sizer();
}

